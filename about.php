<h2>What We Do</h2>
<p>We are replacing  what are known as 3 stone fires, with hand built masonry cook stoves, known locally as estufas, or la plancha. The 3 stone has a fire il plate (often the lid from a 55 gallon drum) laid over the top to cook on. These 3 stone fires are commonly located within a dwelling, providing heat as well.  The problem with this method of cooking and heating is that there is no way of properly venting the wood smoke. These open fires are the sole source of cooking and warmth for thousands of Mayan families, but also the source of much misery.</p>
<h2>About Us</h2>
<p>Masons on a Mission (MOM) is an IRS tax exempt charitable foundation whose mission is mobilize North American masons and other interested parties to build safe masonry cook stoves for impoverished Maya in Central America, and to train local Maya in cook stove construction in order to improve public health in the region.</p>
