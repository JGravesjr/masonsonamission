<h3>What We Do</h3>
<p>Masons on a Mission works with Emily Webb of the Calacyria Foundation, an American nonprofit, to fund and build vented cookstoves in villages around Lake Atitlan in Guatemala.</p>
<h3>Our Volunteers</h3>
<p>We are an international group of volunteers, some with masonry experience, and some without. We spend one week a year building estufas in Guatemala and the rest of the year fundraising and spreading awareness about this life-changing project.</p>
